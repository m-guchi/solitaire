/** ISO 形式 (YYYY-MM-DD) を表示用に整形 */
export function formatChangelogDate(date) {
  const full = /^(\d{4})-(\d{2})-(\d{2})$/.exec(date);
  if (full) {
    return `${Number(full[1])}年${Number(full[2])}月${Number(full[3])}日`;
  }
  return date;
}
